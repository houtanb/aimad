  INTEGER, INTENT(IN) :: qcols
  INTEGER, INTENT(IN) :: qrows
  DOUBLE PRECISION, DIMENSION(qrows, qcols), INTENT(INOUT) :: q
  DOUBLE PRECISION, DIMENSION(nbdirsmax, qrows, qcols), INTENT(INOUT) :: qd
  DOUBLE PRECISION, DIMENSION(:, :), ALLOCATABLE :: hpart, hcopy
  DOUBLE PRECISION, DIMENSION(:, :, :), ALLOCATABLE :: hpartd, hdcopy
